       public  Usuarios armausuarios(){
        
          String nombres_usu    = txtNombre.getText().trim();
          String apellidos_usu  = txtApellidos.getText().trim();
          String usuario        = txtUsuario.getText().trim();
          String contrasena     = txtContrasena.getText().trim();
          String identifica     = identificacion.getText().trim();
              
           if(nombres_usu.equals("")){
              JOptionPane.showMessageDialog(new JDialog(), "Digite el Nombre");
             }else{
                    if(apellidos_usu.equals("")){
                        JOptionPane.showMessageDialog(new JDialog(), "Digite el Usuario");
                       }else{
                            if(usuario.equals("")){
                            JOptionPane.showMessageDialog(new JDialog(), "Digite el Nombre de Usuario");
                             }else{
                                   if(contrasena.equals("")){
                                       JOptionPane.showMessageDialog(new JDialog(), "Digite la Contraseña");
                                       }else{
                                   }               
                        }
                     }
                 }
          Usuarios Usuarios = new Usuarios (nombres_usu, apellidos_usu, usuario, contrasena, identifica);
        
          return Usuarios;
    }
    
