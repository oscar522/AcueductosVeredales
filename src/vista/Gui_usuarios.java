    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        // TODO add your handling code here:
        
        if (identificacion.getText().equals("")){

            JOptionPane.showMessageDialog(new JDialog(), "Debe digitar el numero de Identificacion");
        }else{
         
            String DOCUMENTO = identificacion.getText();
            datos=s.ConsultarUsuario(DOCUMENTO);
            txtNombre.setText(String.valueOf(datos[0][1]));
            txtApellidos.setText(String.valueOf(datos[0][2]));
            txtUsuario.setText(String.valueOf(datos[0][3]));
            txtContrasena.setText(String.valueOf(datos[0][4]));
            identificacion.setText(String.valueOf(datos[0][0]));
            
       
        }
    }//GEN-LAST:event_jLabel3MouseClicked

